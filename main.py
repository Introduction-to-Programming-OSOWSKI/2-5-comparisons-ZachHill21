def greaterThan(x, y):
    if x > y :
        return True
    else:
        return False
print (greaterThan(6, 10))

def lessThan(x, y):
    if x < y :
        return True
    else:
        return False
print (lessThan(6, 10))

def equalTo(x, y):
    if x == y :
        return True
    else:
        return False
print (equalTo(6, 10))

def greaterOrEqual(x, y):
    if x >= y:
        return True
    else:
        return False
print (greaterOrEqual(6, 10))

def lessOrEqual(x, y):
    if x <= y :
        return True
    else:
        return False
print (lessOrEqual(6, 10))
